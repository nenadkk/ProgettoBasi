-- QUERY 1: Contare il numero di reparti per ciascun ospedale.
SELECT O.Codice AS CodiceOspedale, COUNT(R.Codice) AS NumeroReparti
FROM Ospedale O
JOIN Reparto R ON O.Codice = R.Ospedale
GROUP BY O.Codice
ORDER BY NumeroReparti DESC;

-- QUERY 2 Trovare tutte le persone, indicandone nome e cognome, che hanno partorito dopo una certa data (ad es. 2024-08-15) e mostrare la regione e la data del parto.
SELECT p.Nome, p.Cognome, pa.Data_Parto, o.Regione
FROM Parto pa
JOIN Persona p ON pa.Persona = p.CF
JOIN Ostetricia os ON pa.Ospedale = os.Ospedale AND pa.Reparto = os.Reparto
JOIN Reparto r ON os.Ospedale = r.Ospedale AND os.Reparto = r.Codice
JOIN Ospedale o ON r.Ospedale = o.Codice
WHERE pa.Data_Parto > '2024-08-15'
ORDER BY pa.Data_Parto DESC;

-- QUERY 3 Calcolare il numero medio di letti nei reparti di Ostetricia per ciascuna regione.
SELECT O.Regione, ROUND(AVG(Os.Numero_Letti),2) AS MediaLettiOstetriciaPerRegione
FROM Ospedale O
JOIN Reparto R ON O.Codice = R.Ospedale
JOIN Ostetricia Os ON R.Ospedale = Os.Ospedale AND R.Codice = Os.Reparto
GROUP BY O.Regione
ORDER BY MediaLettiOstetriciaPerRegione DESC;

-- QUERY 4 Elenca le persone che hanno avuto più di un parto
SELECT P.Nome, P.Cognome, COUNT(Pa.Data_Parto) AS NumeroParti
FROM Persona P
JOIN Parto Pa ON P.CF = Pa.Persona
GROUP BY P.CF, P.Nome, P.Cognome
HAVING COUNT(Pa.Data_Parto) > 1
ORDER BY NumeroParti DESC, P.Cognome, P.Nome;

-- QUERY 5: Trovare le regioni con la media più alta di ricoveri per reparto
SELECT O.Regione, COUNT(R.Codice) AS Numero_Reparti, SUM(R.N_Ricoveri) AS Totale_Ricoveri, ROUND(AVG(R.N_Ricoveri),2) AS Media_Ricoveri_Per_Reparto
FROM Ospedale O JOIN Reparto R ON O.Codice = R.Ospedale
GROUP BY O.Regione
ORDER BY Media_Ricoveri_Per_Reparto DESC;

