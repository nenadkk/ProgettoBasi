CREATE TABLE Ospedale (
    Codice INT PRIMARY KEY,
    N_Dipendenti INT,
    Regione VARCHAR(100) NOT NULL
);

CREATE TABLE Privato (
    Ospedale INT PRIMARY KEY,
    Gruppo VARCHAR(100) NOT NULL,
    FOREIGN KEY (Ospedale) REFERENCES Ospedale(Codice)
);

CREATE TABLE Universita (
    Partita_IVA VARCHAR(16) PRIMARY KEY,
    Nome VARCHAR(100) NOT NULL,
    Indirizzo VARCHAR(255) NOT NULL
);

CREATE TABLE Publico (
    Ospedale INT PRIMARY KEY,
    Anno_Fondazione INT NOT NULL,
    Universita_PartitaIVA VARCHAR(16),
    FOREIGN KEY (Ospedale) REFERENCES Ospedale(Codice),
	FOREIGN KEY (Universita_PartitaIVA) REFERENCES Universita(Partita_IVA)
);

CREATE TABLE Persona (
    CF VARCHAR(16) PRIMARY KEY,
    Nome VARCHAR(100) NOT NULL,
    Cognome VARCHAR(100) NOT NULL
);

CREATE TABLE Dirigente (
    Persona_CF VARCHAR(16),
    Privato INT,
    Ruolo VARCHAR(100) NOT NULL,
    Data_Inizio DATE NOT NULL,
    PRIMARY KEY (Persona_CF, Privato),
    FOREIGN KEY (Persona_CF) REFERENCES Persona(CF),
    FOREIGN KEY (Privato) REFERENCES Privato(Ospedale)
);


CREATE TABLE Rettore (
    Persona_CF VARCHAR(16),
    Universita_PartitaIVA VARCHAR(16),
    Data_Inizio DATE NOT NULL,
    PRIMARY KEY (Persona_CF, Universita_PartitaIVA),
    FOREIGN KEY (Persona_CF) REFERENCES Persona(CF),
    FOREIGN KEY (Universita_PartitaIVA) REFERENCES Universita(Partita_IVA)
);


CREATE TABLE Reparto (
    Ospedale INT,
    Codice INT,
    Dimensione INT NOT NULL,
    N_Ricoveri INT NOT NULL,
    PRIMARY KEY (Ospedale, Codice),
    FOREIGN KEY (Ospedale) REFERENCES Ospedale(Codice)
);

CREATE TABLE Ostetricia (
    Ospedale INT,
    Reparto INT,
    Numero_Letti INT NOT NULL,
    PRIMARY KEY (Ospedale, Reparto),
    FOREIGN KEY (Ospedale, Reparto) REFERENCES Reparto(Ospedale, Codice)
);

CREATE TABLE Parto (
    Persona VARCHAR(16),
    Data_Parto DATE,
    Settimana INT NOT NULL,
    Ospedale INT NOT NULL,
    Reparto INT NOT NULL,
    PRIMARY KEY (Persona, Data_Parto),
    FOREIGN KEY (Persona) REFERENCES Persona(CF),
    FOREIGN KEY (Ospedale, Reparto) REFERENCES Ostetricia(Ospedale, Reparto)
);

CREATE TABLE Ricovero (
    Persona VARCHAR(16),
    Data_Ricovero DATE,
    Posto_Letto INT NOT NULL,
    Ospedale INT NOT NULL,
    Reparto INT NOT NULL,
    PRIMARY KEY (Persona, Data_Ricovero),
    FOREIGN KEY (Persona) REFERENCES Persona(CF),
    FOREIGN KEY (Ospedale, Reparto) REFERENCES Reparto(Ospedale, Codice)
);


